def ft_garden_intro() -> None:
    """
    Displays basic information about a plant in the garden.
    """
    name = "Rose"
    height = 25
    age = 30
    print("=== Welcome to My Garden ===")
    print("Plant:", name)
    print(f"Height: {height}cm")
    print("Age:", age, "days")
    print("\n")
    print("=== End of Program ===")


if __name__ == "__main__":
    ft_garden_intro()
